package model;

public class Amigos {
	private String Usuario;
	private String Amigo;
	public String getUsuario() {
		return Usuario;
	}
	public void setUsuario(String usuario) {
		Usuario = usuario;
	}
	public String getAmigo() {
		return Amigo;
	}
	public void setAmigo(String amigo) {
		Amigo = amigo;
	}



}
